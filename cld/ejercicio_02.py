# Ejercicio 2: Operaciones basicas de calculos y concatenacion de strings

# Definicion de variables
num1 = 15
num2 = 4

# Operaciones Basicas
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(float(num1 / num2))
print(num1 % num2)
print(num1 // num2)
print(num1 ** num2)

# Variables para concatenacion de strings
nombre = 'Gaston'
apellido = 'Buedo'
nombre_completo = nombre + ' ' + apellido
print(f"El nombre completo concatenado es: {nombre_completo}")
print(f"La longitud del completo concatenado es: {len(nombre_completo)}")